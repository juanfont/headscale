func main() {
	if len(os.Args) < 2 {
		fmt.Println("Usage: changegen <source.go>")
		os.Exit(1)
	}

	filename := os.Args[1]
	
	fset := token.NewFileSet()
	node, err := parser.ParseFile(fset, filename, nil, parser.ParseComments)
	if err != nil {
		panic(err)
	}

	data := TemplateData{
		Package: node.Name.Name,
		Structs: []ChangeStruct{},
	}

	ast.Inspect(node, func(n ast.Node) bool {
		if typeSpec, ok := n.(*ast.TypeSpec); ok {
			if structType, ok := typeSpec.Type.(*ast.StructType); ok {
				structName := typeSpec.Name.Name
				
				if strings.Contains(structName, "Change") || structName == "Change" {
					changeStruct := parseChangeStruct(structName, structType)
					data.Structs = append(data.Structs, changeStruct)
				}
			}
		}
		return true
	})

	tmpl := template.Must(template.New("change").Funcs(template.FuncMap{
		"cleanName":       cleanName,
		"cleanSystemName": cleanSystemName,
		"trimSuffix":      strings.TrimSuffix,
	}).Parse(generatedCodeTemplate))
	
	outputFile := strings.Replace(filename, ".go", "_generated.go", 1)
	f, err := os.Create(outputFile)
	if err != nil {
		panic(err)
	}
	defer f.Close()

	err = tmpl.Execute(f, data)
	if err != nil {
		panic(err)
	}

	fmt.Printf("Generated %s\n", outputFile)
}

func parseChangeStruct(name string, structType *ast.StructType) ChangeStruct {
	changeStruct := ChangeStruct{
		Name:   name,
		Fields: []Field{},
	}

	for _, field := range structType.Fields.List {
		if len(field.Names) == 0 {
			continue
		}
		
		fieldName := field.Names[0].Name
		fieldType := getTypeString(field.Type)
		
		tag := "simple"
		if fieldName == "ID" {
			tag = "id"
			changeStruct.IDType = fieldType
		} else if fieldType == "NodeChange" || fieldType == "UserChange" {
			tag = "struct"
		}
		
		changeStruct.Fields = append(changeStruct.Fields, Field{
			Name: fieldName,
			Type: fieldType,
			Tag:  tag,
		})
	}

	return changeStruct
}

func getTypeString(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		return t.Name
	case *ast.StarExpr:
		return "*" + getTypeString(t.X)
	case *ast.SelectorExpr:
		return getTypeString(t.X) + "." + t.Sel.Name
	default:
		return "unknown"
	}
}

func cleanName(fieldName, structName string) string {
	prefix := ""
	if strings.HasPrefix(structName, "Node") {
		prefix = "Node"
	} else if strings.HasPrefix(structName, "User") {
		prefix = "User"
	}
	
	switch {
	case strings.Contains(fieldName, "Online"):
		return prefix + "Online"
	case strings.Contains(fieldName, "Offline"):
		return prefix + "Offline"
	case strings.Contains(fieldName, "New") || strings.Contains(fieldName, "Added"):
		return prefix + "Added"
	case strings.Contains(fieldName, "Removed"):
		return prefix + "Removed"
	case strings.Contains(fieldName, "Key"):
		return prefix + "KeyChanged"
	case strings.Contains(fieldName, "Tags"):
		return prefix + "TagsChanged"
	case strings.Contains(fieldName, "Routes"):
		return prefix + "RoutesChanged"
	case strings.Contains(fieldName, "Expiry"):
		return prefix + "ExpiryChanged"
	case strings.Contains(fieldName, "Hostinfo"):
		return prefix + "HostinfoChanged"
	case strings.Contains(fieldName, "Full"):
		return prefix + "FullUpdate"
	default:
		cleanField := strings.TrimSuffix(fieldName, "Changed")
		return prefix + cleanField
	}
}

func cleanSystemName(fieldName string) string {
	switch {
	case fieldName == "Full":
		return "FullUpdate"
	case strings.HasSuffix(fieldName, "Changed"):
		base := strings.TrimSuffix(fieldName, "Changed")
		return base + "Update"
	default:
		return fieldName + "Update"
	}
}