package main

import (
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"strings"
	"text/template"
)

type Field struct {
	Name string
	Type string
	Tag  string
}

type ChangeStruct struct {
	Name   string
	IDType string
	Fields []Field
}

type TemplateData struct {
	Package string
	Structs []ChangeStruct
}

